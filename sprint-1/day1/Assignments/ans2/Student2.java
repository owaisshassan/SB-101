package sprint1day1assignmentsQues2;

public class Student2 {

	int roll;
	String name;
	int marks;
	
	public Student2(int roll, String name, int marks) {
		super();
		this.roll = roll;
		this.name = name;
		this.marks = marks;
	}

	@Override
	public String toString() {
		return "Student [roll=" + roll + ", name=" + name + ", marks=" + marks + "]";
	}
	
	
}
