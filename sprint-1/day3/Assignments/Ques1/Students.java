package sprint1day3assignments.Ques1;



public class Students {

	int rollNo;
	String name;
	int marks;
	
	
	public Students(int rollNo, String name, int marks) {
		super();
		this.rollNo = rollNo;
		this.name = name;
		this.marks = marks;
	}


	@Override
	public String toString() {
		return "Students [rollNo=" + rollNo + ", name=" + name + ", marks=" + marks + "]";
	}




	
}




	

	
